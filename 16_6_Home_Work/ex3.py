shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]
count = 0
cost = 0

item = input("Название детали: ")

for value in shop:
    if value[0] == item:
        count += 1
        cost += value[1]

print(f"Кол-во деталей - {count}\nОбщая стоимость - {cost}")
